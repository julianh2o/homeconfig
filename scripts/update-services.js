#!/usr/bin/env node

require('dotenv').config();

const http = require('http');
const fs = require('fs');
const path = require('path');

const HA_HOST = '192.168.0.110';
const HA_PORT = 8123;
const HA_TOKEN = process.env.HA_TOKEN;

if (!HA_TOKEN) {
  console.error('Error: HA_TOKEN environment variable is not set');
  console.error('Please set it with: export HA_TOKEN="your_token_here"');
  process.exit(1);
}

async function fetchServices() {
  return new Promise((resolve, reject) => {
    const options = {
      hostname: HA_HOST,
      port: HA_PORT,
      path: '/api/services',
      method: 'GET',
      headers: {
        'Authorization': `Bearer ${HA_TOKEN}`,
        'Content-Type': 'application/json'
      }
    };

    const req = http.request(options, (res) => {
      let data = '';

      res.on('data', (chunk) => {
        data += chunk;
      });

      res.on('end', () => {
        if (res.statusCode === 200) {
          try {
            resolve(JSON.parse(data));
          } catch (e) {
            reject(new Error(`Failed to parse JSON: ${e.message}`));
          }
        } else {
          reject(new Error(`HTTP ${res.statusCode}: ${data}`));
        }
      });
    });

    req.on('error', (e) => {
      reject(e);
    });

    req.end();
  });
}

async function main() {
  try {
    console.log('Fetching services from Home Assistant...');
    const servicesArray = await fetchServices();

    // Generate YAML content
    let yamlContent = '# Available Services\n';
    yamlContent += '# Auto-generated by: npm run update:services\n';
    yamlContent += `# Generated at: ${new Date().toISOString()}\n\n`;

    // Sort domains alphabetically
    const sortedDomains = servicesArray.sort((a, b) => a.domain.localeCompare(b.domain));

    let totalServices = 0;

    sortedDomains.forEach(domainObj => {
      const domain = domainObj.domain;
      const domainServices = domainObj.services;

      yamlContent += `${domain}:\n`;

      const sortedServices = Object.keys(domainServices).sort();

      sortedServices.forEach(serviceName => {
        const service = domainServices[serviceName];
        yamlContent += `  - ${serviceName}`;

        // Add description if available
        if (service.description) {
          yamlContent += `: ${service.description}`;
        }

        yamlContent += '\n';
        totalServices++;
      });

      yamlContent += '\n';
    });

    // Write to file
    const outputPath = path.join(__dirname, '..', 'state', 'services.yaml');
    fs.writeFileSync(outputPath, yamlContent, 'utf8');

    console.log(`\nâœ“ Successfully wrote ${totalServices} services across ${servicesArray.length} domains to ${outputPath}`);

  } catch (error) {
    console.error('Error:', error.message);
    process.exit(1);
  }
}

main();
