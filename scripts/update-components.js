#!/usr/bin/env node

require('dotenv').config();

const http = require('http');
const fs = require('fs');
const path = require('path');

const HA_HOST = '192.168.0.110';
const HA_PORT = 8123;
const HA_TOKEN = process.env.HA_TOKEN;

if (!HA_TOKEN) {
  console.error('Error: HA_TOKEN environment variable is not set');
  console.error('Please set it with: export HA_TOKEN="your_token_here"');
  process.exit(1);
}

async function fetchComponents() {
  return new Promise((resolve, reject) => {
    const options = {
      hostname: HA_HOST,
      port: HA_PORT,
      path: '/api/config',
      method: 'GET',
      headers: {
        'Authorization': `Bearer ${HA_TOKEN}`,
        'Content-Type': 'application/json'
      }
    };

    const req = http.request(options, (res) => {
      let data = '';

      res.on('data', (chunk) => {
        data += chunk;
      });

      res.on('end', () => {
        if (res.statusCode === 200) {
          try {
            const config = JSON.parse(data);
            resolve(config.components || []);
          } catch (e) {
            reject(new Error(`Failed to parse JSON: ${e.message}`));
          }
        } else {
          reject(new Error(`HTTP ${res.statusCode}: ${data}`));
        }
      });
    });

    req.on('error', (e) => {
      reject(e);
    });

    req.end();
  });
}

async function main() {
  try {
    console.log('Fetching components from Home Assistant...');
    const components = await fetchComponents();

    // Generate YAML content
    let yamlContent = '# Installed Components\n';
    yamlContent += '# Auto-generated by: npm run update:components\n';
    yamlContent += `# Generated at: ${new Date().toISOString()}\n\n`;

    // Sort components alphabetically
    const sortedComponents = components.sort();

    sortedComponents.forEach(component => {
      yamlContent += `- ${component}\n`;
    });

    // Write to file
    const outputPath = path.join(__dirname, '..', 'state', 'components.yaml');
    fs.writeFileSync(outputPath, yamlContent, 'utf8');

    console.log(`\nâœ“ Successfully wrote ${sortedComponents.length} components to ${outputPath}`);

  } catch (error) {
    console.error('Error:', error.message);
    process.exit(1);
  }
}

main();
